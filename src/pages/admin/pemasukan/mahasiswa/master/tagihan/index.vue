<script setup>

const selectedThAkademik = ref();
const thAkademik = ref([]);
const selectedThAngkatan = ref();
const thAngkatan = ref([]);
const selectedProdi = ref();
const prodi = ref([]);

const page = ref(1)
const itemsPerPage = ref(5)
const sortBy = ref({ key: 'id', order: 'desc' })
const search = ref("");
const selectedRows = ref([])
const dataTable = ref([])
const totalItems = ref(0)
const loading = ref(true)
const initialLoading = ref(true)

const fetchData = async () => {
  try {
    loading.value = true
    const { data } = await $api('/admin/pemasukan/mahasiswa/tagihan', {
      method: "GET",
      body: {
        page: page.value,
        limit: itemsPerPage.value,
        sort_key: sortBy.value.key,
        sort_order: sortBy.value.order,
        search: search.value,
        ...(selectedThAkademik.value && { th_akademik_id: selectedThAkademik.value }),
        ...(selectedThAngkatan.value && { th_angkatan_id: selectedThAngkatan.value }),
        ...(selectedProdi.value && { prodi_id: selectedProdi.value }),
      },
    })

    dataTable.value = data.data
    totalItems.value = data.total
  } catch (err) {
    console.error(err)
  } finally {
    loading.value = false
    if (initialLoading.value) initialLoading.value = false
  }
}

const fetchThAkademik = async () => {
  try {
    const { data } = await $api('/admin/th-akademik', {
      method: "GET",
      body: {
        limit: 0,
        sort_key: 'kode',
        sort_order: 'desc',
      }
    })

    thAkademik.value = data.data.map(thAkademik => {
      return {
        title: `${thAkademik.nama} - ${thAkademik.semester}`,
        value: thAkademik.id,
      }
    })
  } catch (err) {
    console.error(err)
  }
}
const fetchThAngkatan = async () => {
  try {
    const { data } = await $api('/admin/th-akademik', {
      method: "GET",
      body: {
        limit: 0,
        sort_key: 'kode',
        sort_order: 'desc',
        search: 'ganjil',
      }
    })

    thAngkatan.value = data.data.map(thAngkatan => {
      return {
        title: thAngkatan.kode.slice(0, -1),
        value: thAngkatan.id,
      }
    })
  } catch (err) {
    console.error(err)
  }
}
const fetchProdi = async () => {
  try {
    const { data } = await $api('/admin/prodi', {
      method: "GET",
      body: {
        limit: 0,
        sort_key: 'kode',
        sort_order: 'desc',
      }
    })

    prodi.value = data.data.map(prodi => {
      return {
        title: prodi.nama,
        value: prodi.id,
      }
    })
  } catch (err) {
    console.error(err)
  }
}

const loadItems = ({ page: p, itemsPerPage: ipp, sortBy: sb, search: s }) => {
  loading.value = true
  page.value = p
  itemsPerPage.value = ipp
  if (sb.length) sortBy.value = sb[0]
  fetchData()
}

const isDialogDeleteVisible = ref(false)
const deleteData = ref({})

const showDialogDelete = (id, name) => {
  deleteData.value = {
    id: id,
    name: name
  }
  isDialogDeleteVisible.value = true
}

const deleteDataSubmit = async (id) => {
  try {
    const response = await $api("/admin/pemasukan/mahasiswa/tagihan/" + id, {
      method: 'DELETE',
    });

    if (response.status === true) {
      showSnackbar({
        text: response.message,
        color: 'success',
      });

      fetchData()
    } else {
      showSnackbar({
        text: response.message,
        color: 'error',
      });
    }

  } catch (err) {
    const message = Array.isArray(err.data?.message)
      ? err.data.message.join('; ')
      : err.data?.message || 'Terjadi kesalahan.';

    showSnackbar({
      text: message,
      color: 'error',
    });
  } finally {
    isDialogDeleteVisible.value = false
  }
}

onMounted(() => {
  document.title = 'Tagihan - SIMKEU'
  fetchThAkademik();
  fetchThAngkatan();
  fetchProdi();
})

watch(selectedRows, (newValue) => {
  newValue.forEach((row, index) => {
    console.log(`${index + 1}.`, row)
  })
}, { deep: true })

watch([selectedThAkademik, selectedThAngkatan, selectedProdi], () => {
  fetchData()
})

</script>

<template>

  <div>


    <VRow class="mb-2">
      <!-- ğŸ‘‰ Select Th Akademik -->
      <VCol cols="12" sm="6">
        <VSelect v-model="selectedThAkademik" label="Select Th Akademik" placeholder="Select Th Akademik"
          :items="thAkademik" clearable clear-icon="ri-close-line" class="custom-bg-select" />
      </VCol>
      <!-- ğŸ‘‰ Select Th Angkatan -->
      <VCol cols="12" sm="6">
        <VSelect v-model="selectedThAngkatan" label="Select Th Angkatan" placeholder="Select Th Angkatan"
          :items="thAngkatan" clearable clear-icon="ri-close-line" class="custom-bg-select" />
      </VCol>
      <!-- ğŸ‘‰ Select Prodi -->
      <VCol cols="12" sm="12">
        <VSelect v-model="selectedProdi" label="Select Prodi" placeholder="Select Prodi" :items="prodi" clearable
          clear-icon="ri-close-line" class="custom-bg-select" />
      </VCol>

    </VRow>


    <VCard>
      <VCardItem class="pb-4">
        <VCardTitle>Tagihan</VCardTitle>
      </VCardItem>


      <VDivider />

      <VCardText class="d-flex flex-wrap gap-4">
        <div class="d-flex align-center w-100 w-sm-auto">
          <!-- ğŸ‘‰ Search  -->
          <VTextField v-model="search" placeholder="Search Data" style="inline-size: 200px;" density="compact"
            class="me-3" />
        </div>

        <VSpacer />

        <div class="d-flex gap-x-4 align-center">
          <!-- ğŸ‘‰ Export button -->
          <VBtn variant="outlined" color="secondary" prepend-icon="ri-upload-2-line">
            Export
          </VBtn>

          <VBtn color="primary" prepend-icon="ri-add-line"
            @click="$router.push('/admin/pemasukan/mahasiswa/master/tagihan/add')">
            Add Data
          </VBtn>
        </div>
      </VCardText>

      <!-- ğŸ‘‰ Datatable  -->
      <VDataTableServer :headers="[
        { title: 'No', key: 'id' },
        { title: 'Tahun Akademik', key: 'th_akademik_kode' },
        { title: 'Tahun Angkatan', key: 'th_angkatan_kode' },
        { title: 'Prodi', key: 'prodi_nama' },
        { title: 'Nama', key: 'nama' },
        { title: 'Jumlah', key: 'jumlah' },
        { title: 'Actions', key: 'actions', sortable: false },
      ]" v-model:model-value="selectedRows" v-model:items-per-page="itemsPerPage" v-model:page="page"
        :items="dataTable" :items-length="totalItems" :loading="loading" :search="search" item-value="id"
        @update:options="loadItems">
        <template v-if="initialLoading" #loading>
          <div class="text-center pa-4">
            <VProgressCircular indeterminate color="primary" class="mb-2" />
            <div>Memuat data...</div>
          </div>
        </template>

        <template v-else #no-data>
          <div class="text-center pa-4">Tidak ada data.</div>
        </template>

        <template #item.id="{ index }">
          {{ itemsPerPage * (page - 1) + index + 1 }}
        </template>

        <template #item.nama="{ item }">
          <div style="margin:15px 0">
            <VChip color="success" size="x-small" label>
              {{ item.kode }}
            </VChip>
            <div><b>{{ item.nama }}</b></div>
          </div>
        </template>

        <!-- Actions -->
        <template #item.actions="{ item }">
          <IconBtn size="small">
            <VIcon icon="ri-more-2-fill" />

            <VMenu activator="parent">
              <VList>
                <VListItem value="edit" prepend-icon="ri-edit-box-line"
                  @click="$router.push(`/admin/pemasukan/mahasiswa/master/tagihan/edit/${item.id}`)">
                  Edit
                </VListItem>

                <VListItem value="delete" prepend-icon="ri-delete-bin-line"
                  @click="showDialogDelete(item.id, item.nama)">
                  Delete
                </VListItem>

              </VList>
            </VMenu>
          </IconBtn>
        </template>
      </VDataTableServer>
    </VCard>

    <VDialog v-model="isDialogDeleteVisible" width="500">
      <!-- Dialog Content -->
      <VCard :title="'Hapus Data: ' + deleteData.name">
        <DialogCloseBtn variant="text" size="default" @click="isDialogDeleteVisible = false" />

        <VCardText class="d-flex align-center">
          <VIcon icon="ri-alert-line" size="32" class="me-2" />
          <span>
            Anda yakin ingin menghapus data pengguna ini? Penghapusan data pengguna tidak dapat
            dibatalkan.
          </span>
        </VCardText>

        <VCardText class="d-flex justify-end flex-wrap gap-4">
          <VBtn variant="outlined" color="secondary" @click="isDialogDeleteVisible = false">
            Batal
          </VBtn>
          <VBtn color="error" @click="deleteDataSubmit(deleteData.id)">
            <VIcon icon="ri-delete-bin-line" class="me-1" />
            Hapus
          </VBtn>
        </VCardText>
      </VCard>
    </VDialog>
  </div>
</template>
